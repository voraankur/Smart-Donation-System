    private void populateDonationTable(){
        DefaultTableModel dtm = (DefaultTableModel) DonationCartJTable.getModel();
        dtm.setRowCount(0);
        
        for (Product product: tempProductList){
            Object[] row= new Object[2];
            row[0]= product.getProductName();
        //    row[1]= product.getQuantity();
            row[1]= product.getProductType();
            //order.getSalesPerson().setCommission(order.calculateCommission(orderItem) * orderItem.getQuantity());
            dtm.addRow(row);
        }
    }

