    private void btnAddToDonationCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToDonationCartActionPerformed
        
        if(productNameTxt.getText().trim().length()==0 
                || productDescTxt.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Please fill all the fields.","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        Product product= new Product();
        product.setProductName(productNameTxt.getText());
        product.setProductType(typeComboBox.getSelectedItem().toString());
        product.setProductDesc(productDescTxt.getText());
        //product.setQuantity(Integer.parseInt(quantityTxt.getText()));
        product.setDonor((Donor)userAccount.getEmployee());
        
        tempProductList.add(product);
        
        populateDonationTable();
        
        productNameTxt.setText("");
        productDescTxt.setText("");
        //quantityTxt.setText("");
    }//GEN-LAST:event_btnAddToDonationCartActionPerformed

