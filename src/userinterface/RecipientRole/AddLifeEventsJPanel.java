    private void btnAddLifeEventActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLifeEventActionPerformed
        Recipient recipient= (Recipient)userAccount.getEmployee();
        
        if(lifeEventTextArea.getText().trim().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter value");
        }
        
        LifeEvent lifeEvent= new LifeEvent();
        
        lifeEvent.setLifeEventString(lifeEventTextArea.getText());
        
        recipient.getLifeEventList().getLifeEventList().add(lifeEvent);
        populateLifeEventTable();
        
        lifeEventTextArea.setText("");
    }//GEN-LAST:event_btnAddLifeEventActionPerformed


