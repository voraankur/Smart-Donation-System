    private void populateLifeEventTable(){
        
        Recipient recipient= (Recipient) userAccount.getEmployee();
        
        DefaultTableModel model = (DefaultTableModel) lifeEventsTable.getModel();
        
        model.setRowCount(0);
        
        for(LifeEvent lifeEvent: recipient.getLifeEventList().getLifeEventList()){
            Object[] row = new Object[1];
            row[0]= lifeEvent.getLifeEventString();
            model.addRow(row);
        }
        
    }

